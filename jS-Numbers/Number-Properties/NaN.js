// NaN (Not A Number)
// NaN is a JavaScript reserved word for a number that is not a legal number.
// Already Explained in the 'Intro' directory of jS-Numbers.
// This properites can only be accessed as Number.Properties. Writing anything other than Number will return 'undefined'.

console.log(Number.NaN); // NaN
console.log(100 / "Apple"); // NaN
console.log('30'.NaN); // undefined
