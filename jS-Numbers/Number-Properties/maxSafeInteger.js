// jS MAX_SAFE_INTEGER
// It belongs to jS Number Object.
// Number.MAX_SAFE_INTEGER represents the maximum safe Integer in JavaScript.
// This properites can only be accessed as Number.Properties. Writing anything other than Number will return 'undefined'.
// ES6 Feature
console.log(Number.MAX_SAFE_INTEGER); // 9007199254740991 OR [2^53 - 1]
console.log('30'.MAX_SAFE_INTEGER); // undefined

