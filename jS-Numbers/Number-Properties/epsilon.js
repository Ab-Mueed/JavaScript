// jS EPSILON
// It belongs to jS Number Object.
// Number.EPSILON is the difference between the smallest floating point number greater than 1 and 1.
// This properites can only be accessed as Number.Properties. Writing anything other than Number will return 'undefined'.
// ES6 Feature

console.log(Number.EPSILON); // 2.220446049250313e-16
console.log('30'.EPSILON); // undefined