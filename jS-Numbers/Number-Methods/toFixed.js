// The toFixed() Method
// toFixed() returns a string, with the number written with a specified number of decimals.

let x = 123.456;
console.log(x.toFixed(0)); // 123
console.log(x.toFixed(2)); // 123.46
console.log(x.toFixed(4)); // 123.4560
console.log(x.toFixed(6)); // 123.456000
console.log(typeof (x.toFixed(3))); // String