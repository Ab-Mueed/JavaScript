// Numeric String
// JavaScript will try to convert Strings to Numbers in all Numeric Operations (Apart from +).

let num1 = "100";
let num2 = "10";

let result1 = num1 - num2;
console.log(result1); // 90

let result2 = num1 * num2;
console.log(result2); // 1000

let result3 = num1 / num2;
console.log(result3); // 10
