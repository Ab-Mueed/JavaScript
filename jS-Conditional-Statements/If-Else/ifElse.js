// if-Else Statement
// The 'if' statement is used to specify a block of jS code to be executed if a condition is 'true'.
// The 'else' statement is used to specify the block of code to be executed if the condition is 'false'.

// Syntax
/*
if(condition) {
// block of code to be executed if the condition is 'true'
} else {
// block of code to be executed if the condition is 'false'
}
*/

// Example 1
let hour = 15
if (hour < 18){
    greeting = 'Good Day';
} else {
    greeting = 'Good Evening';
}
console.log(greeting); // Good Day


// Example 2
let age =  23
if(age < 18){
    result = 'Allowed';
} else {
    result = 'Not Allowed';
}
console.log(result); // Not Allowed

