// Converting an Array to a String
// The jS method toString() converts an array to a string of (comma separated) array values.

const fruits = ['Apple', 'Banana', 'Grapes', 'Orange'];
console.log(fruits.toString()); // Apple,Banana,Grapes,Orange;

let fruitContainer = fruits.toString();
console.log(fruitContainer); // Apple,Banana,Grapes,Orange
console.log(typeof(fruitContainer)); // String
